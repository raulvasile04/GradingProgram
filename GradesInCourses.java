package school;

/** Public class that stores the Students name and grade in a course . Used in the GradingProgram **/
public class GradesInCourses {

    public String firstname;
    public String lastname;
    public double gradeInCourse;

    public GradesInCourses(String firstname , String lastname , double gradeInCourse){
        this.firstname = firstname;
        this.lastname = lastname;
        this.gradeInCourse = gradeInCourse;
    }

}
